# Assignment Operators

# Initialize a variable with a value
points = 10
print("Initial points:", points)

# Add 5 to the current value using +=
points += 5
print("After += 5:", points)

# Subtract 3 from the current value using -=
points -= 3
print("After -= 3:", points)

# Multiply the current value by 2 using *=
points *= 2
print("After *= 2:", points)

# Divide the current value by 4 using /=
points /= 4
print("After /= 4:", points)
