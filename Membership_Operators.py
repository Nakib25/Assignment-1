# Membership Operators

# Create a list of vegetables
vegetables = ["carrot", "potato", "tomato", "onion"]

# Check if "cucumber" is in the list
print("Is 'cucumber' in the list?", "cucumber" in vegetables)

# Check if "onion" is in the list
print("Is 'onion' in the list?", "onion" in vegetables)

# Check if "tomatotomato" is not in the list
print("Is ' tomato ' not in the list?", "tomato" not in vegetables)
